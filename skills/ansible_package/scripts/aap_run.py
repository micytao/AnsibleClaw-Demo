#!/usr/bin/env python3
"""AAP Controller helper for ansible.builtin.package.

Generated by AnsibleClaw -- uses only Python stdlib (urllib + json).
Launches ad-hoc commands or job templates via the AAP Controller REST API,
polls for completion, and prints structured JSON output.

Environment variables:
    AAP_CONTROLLER_URL    Base URL of the AAP/AWX controller (required)
    AAP_CONTROLLER_TOKEN  OAuth2 bearer token (required)
    AAP_VERIFY_SSL        Set to 'false' to skip TLS verification (default: true)
    AAP_DEFAULT_INVENTORY Inventory name or ID to use when not specified
    AAP_DEFAULT_CREDENTIAL Credential name or ID to use when not specified
"""

from __future__ import annotations

import argparse
import json
import os
import ssl
import sys
import time
import urllib.error
import urllib.parse
import urllib.request

MODULE = "ansible.builtin.package"

# ---------------------------------------------------------------------------
# Configuration from environment
# ---------------------------------------------------------------------------

def _cfg():
    url = os.environ.get("AAP_CONTROLLER_URL", "").rstrip("/")
    token = os.environ.get("AAP_CONTROLLER_TOKEN", "")
    if not url or not token:
        print(
            "Error: AAP_CONTROLLER_URL and AAP_CONTROLLER_TOKEN must be set.",
            file=sys.stderr,
        )
        sys.exit(1)
    verify = os.environ.get("AAP_VERIFY_SSL", "true").lower() in ("true", "1", "yes")
    return url, token, verify


def _ssl_context(verify: bool) -> ssl.SSLContext | None:
    if not verify:
        ctx = ssl.create_default_context()
        ctx.check_hostname = False
        ctx.verify_mode = ssl.CERT_NONE
        return ctx
    return None


# Cache the detected API prefix so we only probe once per run.
_api_prefix: str | None = None


def _detect_api_prefix(base_url: str, token: str, verify: bool) -> str:
    """Probe /api/v2 and /api/controller/v2 (AAP 2.5 gateway) and return
    whichever responds first. The result is cached for the process lifetime."""
    global _api_prefix
    if _api_prefix is not None:
        return _api_prefix
    for prefix in ("/api/v2", "/api/controller/v2"):
        try:
            _request("GET", f"{base_url}{prefix}/ping/", token, verify, fatal=False)
            _api_prefix = prefix
            return prefix
        except AAPRequestError:
            continue
    _api_prefix = "/api/v2"
    return _api_prefix


def _api(base_url: str, token: str, verify: bool, path: str) -> str:
    """Build a full API URL using the auto-detected prefix.

    ``path`` should start with ``/`` and be relative to the API root,
    e.g. ``/inventories/``, ``/job_templates/42/launch/``.
    """
    prefix = _detect_api_prefix(base_url, token, verify)
    return f"{base_url}{prefix}{path}"


