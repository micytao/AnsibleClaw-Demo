_BAKED_URL = "https://aap-aap.apps.cluster-rmf2p-1.dynamic.redhatworkshops.io"
_BAKED_VERIFY_SSL = "true"
_BAKED_INVENTORY = "Demo Inventory"
_BAKED_CREDENTIAL = "Demo Credential"
_BAKED_PROJECT = "AnsibleClaw"
_BAKED_ORGANIZATION = "AnsibleClaw Demo"
_BAKED_EE = "Default execution environment"

# ---------------------------------------------------------------------------
# Configuration: env var > baked value > error
# ---------------------------------------------------------------------------

def _cfg():
    url = (os.environ.get("AAP_CONTROLLER_URL") or _BAKED_URL).rstrip("/")
    token = os.environ.get("AAP_CONTROLLER_TOKEN", "")
    if not url or not token:
        print(
            "Error: AAP_CONTROLLER_URL and AAP_CONTROLLER_TOKEN must be set.\n"
            "  URL:   " + (url or "(not set)") + "\n"
            "  Token: " + ("(set)" if token else "(not set)"),
            file=sys.stderr,
        )
        sys.exit(1)
    verify_str = os.environ.get("AAP_VERIFY_SSL") or _BAKED_VERIFY_SSL or "true"
    verify = verify_str.lower() in ("true", "1", "yes")
    return url, token, verify


def _default_inventory():
    return os.environ.get("AAP_DEFAULT_INVENTORY") or _BAKED_INVENTORY or ""


def _default_credential():
    return os.environ.get("AAP_DEFAULT_CREDENTIAL") or _BAKED_CREDENTIAL or ""


def _default_project():
    return os.environ.get("AAP_DEFAULT_PROJECT") or _BAKED_PROJECT or ""


def _default_organization():
    return os.environ.get("AAP_DEFAULT_ORGANIZATION") or _BAKED_ORGANIZATION or "Default"


def _default_ee():
    return os.environ.get("AAP_DEFAULT_EE") or _BAKED_EE or ""


def _ssl_context(verify: bool) -> ssl.SSLContext | None:
    if not verify:
        ctx = ssl.create_default_context()
        ctx.check_hostname = False
        ctx.verify_mode = ssl.CERT_NONE
        return ctx
    return None


_api_prefix: str | None = None


def _detect_api_prefix(base_url: str, token: str, verify: bool) -> str:
    global _api_prefix
    if _api_prefix is not None:
        return _api_prefix
    for prefix in ("/api/v2", "/api/controller/v2"):
        try:
            _request("GET", f"{base_url}{prefix}/ping/", token, verify, fatal=False)
            _api_prefix = prefix
            return prefix
        except AAPRequestError:
            continue
    _api_prefix = "/api/v2"
    return _api_prefix


def _api(base_url: str, token: str, verify: bool, path: str) -> str:
    prefix = _detect_api_prefix(base_url, token, verify)
    return f"{base_url}{prefix}{path}"


# ---------------------------------------------------------------------------
# HTTP helpers
# ---------------------------------------------------------------------------

class AAPRequestError(Exception):
    pass


def _request(method: str, url: str, token: str, verify: bool,
             data: dict | None = None, *, fatal: bool = True) -> dict:
    headers = {
        "Authorization": f"Bearer {token}",
        "Content-Type": "application/json",
    }
    body = json.dumps(data).encode() if data else None
    req = urllib.request.Request(url, data=body, headers=headers, method=method)
    ctx = _ssl_context(verify)
    try:
        with urllib.request.urlopen(req, context=ctx, timeout=30) as resp:
            raw = resp.read().decode()
            return json.loads(raw) if raw.strip() else {}
    except urllib.error.HTTPError as exc:
        detail = exc.read().decode() if exc.fp else ""
        msg = f"HTTP {exc.code} from {url}: {detail}"
        if fatal:
            print(msg, file=sys.stderr)
            sys.exit(1)
        raise AAPRequestError(msg)
    except urllib.error.URLError as exc:
        msg = f"Connection error: {exc.reason}"
        if fatal:
            print(msg, file=sys.stderr)
            sys.exit(1)
        raise AAPRequestError(msg)


def _get(url: str, token: str, verify: bool) -> dict:
    return _request("GET", url, token, verify)


def _post(url: str, token: str, verify: bool, data: dict) -> dict:
    return _request("POST", url, token, verify, data)


# ---------------------------------------------------------------------------
# Resource lookup helpers
# ---------------------------------------------------------------------------

def _resolve_id(base_url: str, token: str, verify: bool,
                resource: str, name_or_id: str) -> int:
    if name_or_id.isdigit():
        return int(name_or_id)
    url = _api(base_url, token, verify, f"/{resource}/?name={urllib.parse.quote(name_or_id)}")
    result = _get(url, token, verify)
    items = result.get("results", [])
    if not items:
        print(f"Error: {resource} '{name_or_id}' not found.", file=sys.stderr)
        sys.exit(1)
    return items[0]["id"]


# ---------------------------------------------------------------------------
# Job polling
# ---------------------------------------------------------------------------

TERMINAL_STATES = {"successful", "failed", "error", "canceled"}

def _poll_job(base_url: str, token: str, verify: bool,
              job_url: str, interval: float = 3.0) -> dict:
    full_url = job_url if job_url.startswith("http") else f"{base_url}{job_url}"
    while True:
        job = _get(full_url, token, verify)
        status = job.get("status", "unknown")
        if status in TERMINAL_STATES:
            return job
        print(f"  Status: {status} ... waiting {interval}s", file=sys.stderr)
        time.sleep(interval)


def _get_stdout(base_url: str, token: str, verify: bool,
                job_url: str) -> str:
    full_url = job_url if job_url.startswith("http") else f"{base_url}{job_url}"
    stdout_url = f"{full_url}stdout/?format=json"
    result = _get(stdout_url, token, verify)
    return result.get("content", result.get("result_stdout", ""))


