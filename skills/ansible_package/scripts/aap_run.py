def main() -> None:
    parser = argparse.ArgumentParser(
        prog="aap_run.py",
        description=f"AAP Controller helper for {MODULE}. "
                    "Generic OS package manager",
    )
    sub = parser.add_subparsers(dest="command", required=True)

    # -- adhoc --
    adhoc = sub.add_parser("adhoc", help=f"Run {MODULE} as an ad-hoc command via AAP")
    adhoc.add_argument("module_args", help='Module arguments (e.g. "name=nginx state=present")')
    adhoc.add_argument("--inventory", help="Inventory name or ID")
    adhoc.add_argument("--credential", help="Credential name or ID")
    adhoc.add_argument("--limit", help="Limit to specific host pattern")
    adhoc.add_argument("--no-become", action="store_true", help="Disable become/sudo")
    adhoc.add_argument("--check", action="store_true", help="Run in check/dry-run mode")
    adhoc.set_defaults(func=cmd_adhoc)

    # -- launch --
    launch = sub.add_parser("launch", help="Launch a job template via AAP")
    launch.add_argument("template", help="Job template name or ID")
    launch.add_argument("--extra-vars", help='Extra variables as JSON string')
    launch.add_argument("--limit", help="Limit to specific host pattern")
    launch.add_argument("--inventory", help="Override inventory (name or ID)")
    launch.set_defaults(func=cmd_launch)

    # -- create-jt --
    create_jt = sub.add_parser("create-jt", help="Create a Job Template in AAP for this skill")
    create_jt.add_argument("--name", help=f"Job Template name (default: {MODULE.replace('.', '-')})")
    create_jt.add_argument("--project", help="Project name or ID")
    create_jt.add_argument("--playbook", help="Playbook path within the project")
    create_jt.add_argument("--inventory", help="Inventory name or ID")
    create_jt.add_argument("--credential", help="Credential name or ID")
    create_jt.add_argument("--ee", help="Execution Environment name or ID")
    create_jt.set_defaults(func=cmd_create_jt)

    # -- status --
    status = sub.add_parser("status", help="Check the status of a job")
    status.add_argument("job_id", type=int, help="Job ID to check")
    status.set_defaults(func=cmd_status)

    args = parser.parse_args()
    args.func(args)


if __name__ == "__main__":
    main()
