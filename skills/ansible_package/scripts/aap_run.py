#!/usr/bin/env python3
"""AAP Controller helper for ansible.builtin.package.

Generated by AnsibleClaw -- uses only Python stdlib (urllib + json).
Launches ad-hoc commands, job templates, or creates new job templates
via the AAP Controller REST API. Polls for completion and prints JSON.

Environment variables (override baked defaults):
    AAP_CONTROLLER_URL    Base URL of the AAP/AWX controller
    AAP_CONTROLLER_TOKEN  OAuth2 bearer token (REQUIRED, never baked)
    AAP_VERIFY_SSL        Set to 'false' to skip TLS verification
    AAP_DEFAULT_INVENTORY Inventory name or ID
    AAP_DEFAULT_CREDENTIAL Credential name or ID
"""

