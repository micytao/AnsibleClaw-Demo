def cmd_adhoc(args: argparse.Namespace) -> None:
    """Launch an ad-hoc command for ansible.builtin.package."""
    base_url, token, verify = _cfg()

    inventory = args.inventory or os.environ.get("AAP_DEFAULT_INVENTORY", "")
    credential = args.credential or os.environ.get("AAP_DEFAULT_CREDENTIAL", "")
    if not inventory:
        print("Error: --inventory or AAP_DEFAULT_INVENTORY is required.", file=sys.stderr)
        sys.exit(1)
    if not credential:
        print("Error: --credential or AAP_DEFAULT_CREDENTIAL is required.", file=sys.stderr)
        sys.exit(1)

    inv_id = _resolve_id(base_url, token, verify, "inventories", inventory)
    cred_id = _resolve_id(base_url, token, verify, "credentials", credential)

    payload = {
        "inventory": inv_id,
        "credential": cred_id,
        "module_name": MODULE,
        "module_args": args.module_args,
        "limit": args.limit or "",
        "become_enabled": not args.no_become,
    }

    if args.check:
        payload["diff_mode"] = True
        payload["extra_vars"] = json.dumps({"ansible_check_mode": True})

    print(f"==> Launching ad-hoc: {MODULE} on inventory {inv_id}", file=sys.stderr)
    result = _post(_api(base_url, token, verify, "/ad_hoc_commands/"), token, verify, payload)

    job_url = result.get("url", f"{_detect_api_prefix(base_url, token, verify)}/ad_hoc_commands/{result['id']}/")
    print(f"==> Job URL: {base_url}{job_url}", file=sys.stderr)

    job = _poll_job(base_url, token, verify, job_url)
    stdout = _get_stdout(base_url, token, verify, job_url)

    output = {
        "status": job.get("status"),
        "id": job.get("id"),
        "module": MODULE,
        "module_args": args.module_args,
        "stdout": stdout,
        "failed": job.get("failed", False),
    }
    print(json.dumps(output, indent=2))
    sys.exit(1 if job.get("failed") else 0)


def cmd_launch(args: argparse.Namespace) -> None:
    """Launch a job template."""
    base_url, token, verify = _cfg()

    template_id = _resolve_id(base_url, token, verify,
                              "job_templates", args.template)

    payload: dict = {}
    if args.extra_vars:
        payload["extra_vars"] = args.extra_vars
    if args.limit:
        payload["limit"] = args.limit
    if args.inventory:
        inv_id = _resolve_id(base_url, token, verify, "inventories", args.inventory)
        payload["inventory"] = inv_id

    print(f"==> Launching job template {template_id}", file=sys.stderr)
    result = _post(
        _api(base_url, token, verify, f"/job_templates/{template_id}/launch/"),
        token, verify, payload,
    )

    job_url = result.get("url", f"{_detect_api_prefix(base_url, token, verify)}/jobs/{result['id']}/")
    print(f"==> Job URL: {base_url}{job_url}", file=sys.stderr)

    job = _poll_job(base_url, token, verify, job_url)
    stdout = _get_stdout(base_url, token, verify, job_url)

    output = {
        "status": job.get("status"),
        "id": job.get("id"),
        "template": args.template,
        "stdout": stdout,
        "failed": job.get("failed", False),
    }
    print(json.dumps(output, indent=2))
    sys.exit(1 if job.get("failed") else 0)


def cmd_status(args: argparse.Namespace) -> None:
    """Check the status of a job."""
    base_url, token, verify = _cfg()
    job_path = f"/jobs/{args.job_id}/"
    job = _get(_api(base_url, token, verify, job_path), token, verify)

    output = {
        "status": job.get("status"),
        "id": job.get("id"),
        "started": job.get("started"),
        "finished": job.get("finished"),
        "failed": job.get("failed", False),
    }
    if job.get("status") in TERMINAL_STATES:
        stdout = _get_stdout(base_url, token, verify,
                             _api(base_url, token, verify, job_path))
        output["stdout"] = stdout

    print(json.dumps(output, indent=2))


