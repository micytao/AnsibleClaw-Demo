def cmd_launch(args: argparse.Namespace) -> None:
    """Launch a job template."""
    base_url, token, verify = _cfg()

    template_id = _resolve_id(base_url, token, verify,
                              "job_templates", args.template)

    payload: dict = {}
    if args.extra_vars:
        payload["extra_vars"] = args.extra_vars
    if args.limit:
        payload["limit"] = args.limit
    if args.inventory:
        inv_id = _resolve_id(base_url, token, verify, "inventories", args.inventory)
        payload["inventory"] = inv_id

    print(f"==> Launching job template {template_id}", file=sys.stderr)
    result = _post(
        _api(base_url, token, verify, f"/job_templates/{template_id}/launch/"),
        token, verify, payload,
    )

    job_url = result.get("url", f"{_detect_api_prefix(base_url, token, verify)}/jobs/{result['id']}/")
    print(f"==> Job URL: {base_url}{job_url}", file=sys.stderr)

    job = _poll_job(base_url, token, verify, job_url)
    stdout = _get_stdout(base_url, token, verify, job_url)

    output = {
        "status": job.get("status"),
        "id": job.get("id"),
        "template": args.template,
        "stdout": stdout,
        "failed": job.get("failed", False),
    }
    print(json.dumps(output, indent=2))
    sys.exit(1 if job.get("failed") else 0)


def cmd_create_jt(args: argparse.Namespace) -> None:
    """Create a Job Template in AAP for this composite skill's playbook."""
    base_url, token, verify = _cfg()

    name = args.name or SKILL_NAME.replace("_", "-")
    project = args.project or _default_project()
    inventory = args.inventory or _default_inventory()
    credential = args.credential or _default_credential()
    ee = args.ee or _default_ee()
    playbook = args.playbook or f"skills/ansible_{SKILL_NAME}/assets/playbook.yml"

    if not project:
        print("Error: --project is required (no default configured).", file=sys.stderr)
        sys.exit(1)
    if not inventory:
        print("Error: --inventory is required (no default configured).", file=sys.stderr)
        sys.exit(1)

    project_id = _resolve_id(base_url, token, verify, "projects", project)
    inv_id = _resolve_id(base_url, token, verify, "inventories", inventory)

    payload: dict = {
        "name": name,
        "project": project_id,
        "playbook": playbook,
        "inventory": inv_id,
    }

    if ee:
        ee_id = _resolve_id(base_url, token, verify, "execution_environments", ee)
        payload["execution_environment"] = ee_id

    print(f"==> Creating Job Template '{name}'", file=sys.stderr)
    print(f"    Project: {project} (ID: {project_id})", file=sys.stderr)
    print(f"    Playbook: {playbook}", file=sys.stderr)
    print(f"    Inventory: {inventory} (ID: {inv_id})", file=sys.stderr)

    result = _post(_api(base_url, token, verify, "/job_templates/"), token, verify, payload)
    template_id = result.get("id", "")

    if credential:
        cred_id = _resolve_id(base_url, token, verify, "credentials", credential)
        cred_url = _api(base_url, token, verify,
                        f"/job_templates/{template_id}/credentials/")
        try:
            _post(cred_url, token, verify, {"id": cred_id})
            print(f"    Credential: {credential} (ID: {cred_id}) attached", file=sys.stderr)
        except SystemExit:
            print(f"    Warning: could not attach credential {credential}", file=sys.stderr)

    output = {
        "status": "created",
        "id": template_id,
        "name": name,
        "playbook": playbook,
        "url": f"{base_url}/#/templates/job_template/{template_id}/details",
    }
    print(json.dumps(output, indent=2))


def cmd_status(args: argparse.Namespace) -> None:
    """Check the status of a job."""
    base_url, token, verify = _cfg()
    job_path = f"/jobs/{args.job_id}/"
    job = _get(_api(base_url, token, verify, job_path), token, verify)

    output = {
        "status": job.get("status"),
        "id": job.get("id"),
        "started": job.get("started"),
        "finished": job.get("finished"),
        "failed": job.get("failed", False),
    }
    if job.get("status") in TERMINAL_STATES:
        stdout = _get_stdout(base_url, token, verify,
                             _api(base_url, token, verify, job_path))
        output["stdout"] = stdout

    print(json.dumps(output, indent=2))


