_BAKED_URL = "https://aap-aap.apps.cluster-rmf2p-1.dynamic.redhatworkshops.io"
_BAKED_VERIFY_SSL = "true"
_BAKED_INVENTORY = ""
_BAKED_CREDENTIAL = ""
_BAKED_PROJECT = "AnsibleClaw"
_BAKED_ORGANIZATION = "Ansible Product Demos (APD)"
_BAKED_EE = "Default execution environment"

# ---------------------------------------------------------------------------
# Configuration: env var > baked value > error
# ---------------------------------------------------------------------------

def _cfg():
    url = (os.environ.get("AAP_CONTROLLER_URL") or _BAKED_URL).rstrip("/")
    token = os.environ.get("AAP_CONTROLLER_TOKEN", "")
    if not url or not token:
        print(
            "Error: AAP_CONTROLLER_URL and AAP_CONTROLLER_TOKEN must be set.\n"
            "  URL:   " + (url or "(not set)") + "\n"
            "  Token: " + ("(set)" if token else "(not set)"),
            file=sys.stderr,
        )
        sys.exit(1)
    verify_str = os.environ.get("AAP_VERIFY_SSL") or _BAKED_VERIFY_SSL or "true"
    verify = verify_str.lower() in ("true", "1", "yes")
    return url, token, verify


def _default_inventory():
    return os.environ.get("AAP_DEFAULT_INVENTORY") or _BAKED_INVENTORY or ""


def _default_credential():
    return os.environ.get("AAP_DEFAULT_CREDENTIAL") or _BAKED_CREDENTIAL or ""


def _default_project():
    return os.environ.get("AAP_DEFAULT_PROJECT") or _BAKED_PROJECT or ""


def _default_organization():
    return os.environ.get("AAP_DEFAULT_ORGANIZATION") or _BAKED_ORGANIZATION or "Default"


def _default_ee():
    return os.environ.get("AAP_DEFAULT_EE") or _BAKED_EE or ""


def _ssl_context(verify: bool) -> ssl.SSLContext | None:
    if not verify:
        ctx = ssl.create_default_context()
        ctx.check_hostname = False
        ctx.verify_mode = ssl.CERT_NONE
        return ctx
    return None


# Cache the detected API prefix so we only probe once per run.
_api_prefix: str | None = None


def _detect_api_prefix(base_url: str, token: str, verify: bool) -> str:
    """Probe /api/v2 and /api/controller/v2 (AAP 2.5 gateway) and return
    whichever responds first. The result is cached for the process lifetime."""
    global _api_prefix
    if _api_prefix is not None:
        return _api_prefix
    for prefix in ("/api/v2", "/api/controller/v2"):
        try:
            _request("GET", f"{base_url}{prefix}/ping/", token, verify, fatal=False)
            _api_prefix = prefix
            return prefix
        except AAPRequestError:
            continue
    _api_prefix = "/api/v2"
    return _api_prefix


def _api(base_url: str, token: str, verify: bool, path: str) -> str:
    """Build a full API URL using the auto-detected prefix."""
    prefix = _detect_api_prefix(base_url, token, verify)
    return f"{base_url}{prefix}{path}"


